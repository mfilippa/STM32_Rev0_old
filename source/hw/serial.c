void SERIAL_Initialize(void){
	UART_Init(19200);
}

// -----------------------------------------------------------------------------
// write 8 bit value
// -----------------------------------------------------------------------------
void SERIAL_WriteByte(uint16_t data){
	UART_WriteByte(data);
}

// -----------------------------------------------------------------------------
// write 16 bit value
// -----------------------------------------------------------------------------
void SERIAL_WriteWord(uint16_t data){
	UART_WriteWord(data);
}

// -----------------------------------------------------------------------------
// read 8 bit value
// -----------------------------------------------------------------------------
uint16_t SERIAL_ReadByte(void){
	return UART_ReadByte();
}

// -----------------------------------------------------------------------------
// initialize module
// -----------------------------------------------------------------------------
uint16_t SERIAL_ReadWord(void){
	return UART_ReadWord();
}
