// -----------------------------------------------------------------------------
void IO_Set(int io){
	*(pOut+io+10) = 1;
}
// -----------------------------------------------------------------------------
//
// -----------------------------------------------------------------------------
void IO_Reset(int io){
	*(pOut+io+10) = 0;
}
// -----------------------------------------------------------------------------
//
// -----------------------------------------------------------------------------
void IO_Toggle(int io){
	if (*(pOut+io+10) == 1) *(pOut+io+10) = 0;
	else *(pOut+io+10) = 1;
}
// -----------------------------------------------------------------------------
//
// -----------------------------------------------------------------------------
int IO_ReadInput(int io){
	return *(pIn+io+10);
}
// -----------------------------------------------------------------------------
//
// -----------------------------------------------------------------------------
int IO_ReadOutput(int io){
	return *(pOut+io+10);
}
